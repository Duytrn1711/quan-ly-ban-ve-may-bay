CREATE TABLE AUDIT_LOG(
	LOG_ID INT PRIMARY KEY,
	HANH_DONG NVARCHAR(50),
	THOI_GIAN_TAO DATETIME DEFAULT GETDATE(),
	MO_TA NVARCHAR(200)
);
