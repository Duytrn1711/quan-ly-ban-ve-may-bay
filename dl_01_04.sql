CREATE OR ALTER PROC SP_LOGIN
    @USERNAME VARCHAR(50),
    @MAT_KHAU VARCHAR(50)
AS
BEGIN
    -- Kiểm tra tài khoản tồn tại
    IF NOT EXISTS (
        SELECT 1 FROM NGUOI_DUNG 
        WHERE USERNAME = @USERNAME
    )
    BEGIN
        PRINT N'Tài khoản không tồn tại';
        RETURN;
    END

    -- Kiểm tra mật khẩu
    IF NOT EXISTS (
        SELECT 1 FROM NGUOI_DUNG 
        WHERE USERNAME = @USERNAME AND MAT_KHAU = @MAT_KHAU
    )
    BEGIN
        PRINT N'Sai mật khẩu';
        RETURN;
    END

    -- Login thành công
    SELECT 
        USERNAME,
        VAI_TRO,
        MA_KH,
        MA_NV
    FROM NGUOI_DUNG
    WHERE USERNAME = @USERNAME AND MAT_KHAU = @MAT_KHAU;

    PRINT N'Đăng nhập thành công';
END
